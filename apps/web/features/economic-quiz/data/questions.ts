/**
 * 카테고리 비율을 유지하며 전체 문제에서 count개를 랜덤 선택합니다.
 * STOCK:5, ECONOMY:5, BOND:4, FUND:3, DERIVATIVE:3 = 20문제
 * @param count - 선택할 문제 수 (기본값: 20)
 * @returns 랜덤 선택된 문제 배열
 */
export function selectQuestions(count = 20): QuizQuestion[] {
  const categoryQuota: Record<string, number> = {
    STOCK: 5,
    ECONOMY: 5,
    BOND: 4,
    FUND: 3,
    DERIVATIVE: 3,
  }

  const byCategory: Record<string, QuizQuestion[]> = {
    STOCK: [],
    ECONOMY: [],
    BOND: [],
    FUND: [],
    DERIVATIVE: [],
  }

  for (const q of ALL_QUESTIONS) {
    byCategory[q.category].push(q)
  }

  const selected: QuizQuestion[] = []

  for (const [category, quota] of Object.entries(categoryQuota)) {
    const pool = [...byCategory[category]]
    // Fisher-Yates 셔플
    for (let i = pool.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1))
      ;[pool[i], pool[j]] = [pool[j], pool[i]]
    }
    selected.push(...pool.slice(0, quota))
  }

  // 전체 셔플
  for (let i = selected.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[selected[i], selected[j]] = [selected[j], selected[i]]
  }

  return selected.slice(0, count)
}

